# Programa: Costruir um programma que leia três valores numericos inteiros
# A, B e C, e apresentar como resultado final, armazenado em memoria o valor
# do quadrado soma dos três valores lidos

a = int(input("Informe o valor inteiro de A: "))
b = int(input("Informe o valor inteiro de B: "))
c = int(input("Informe o valor inteiro de C: "))

quadrodo_soma = (a+b+c)^2
print(" o quadrado da soma é: ",quadrodo_soma)
