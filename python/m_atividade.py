# Programa: Construir um programa que leia 3 valores numericos inteiros A, B e C
# E apresentar como resultado final, armazenado em memoria o valor da soma do quadrado dos trÊs valores lidos



a = int(input("Informe o valor inteiro de A: "))
b = int(input("Informe o valor inteiro de B: "))
c = int(input("Informe o valor inteiro de C: "))
soma_quadrados = (a * a) + (b * b) + (c * c)
print("A soma dos quadros é: ", soma_quadrados)