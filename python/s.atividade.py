# Programa: Elaborar um programa que leia dois valores numéricos reais
#  desconhecidos representados pelas variáveis A e B.
#  Calcular, armazenar e apresentar os resultados das quatro
#  operações aritméticas básicas.

# Entrada dos dados
a = int(input("Informe um numero inteiro para letra A: "))
b = int(input("Informe um numero inteiro para letra B: "))

# PROCESSAMENTO DOS DADOS
adicao = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b

# Saída dos dados

print("===RESULTADO DAS 4 OPERAÇÕES BÁSICA ARITMÉTICAS===")
print("ADIÇÃO: ", adicao)
print("Subtração: ", subtracao)
print("Multiplicacao: ", multiplicacao)
print("Divisao: ", divisao)